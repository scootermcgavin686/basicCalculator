def addition(num1,num2):
    return num1 + num2

def subtraction(num1,num2):
    return num1 - num2

def multiplication(num1,num2):
    return num1 * num2

def division(num1,num2):
    return num1 / num2

def modulo(num1,num2):
    userNum = 0
    userQ = num1 // num2
    userNum = num2 * userQ
    return num1 - userNum




